import React, { useState, useEffect, useCallback, useMemo } from "react";
import { useNavigate, useLocation } from "react-router-dom";
import { useGame } from "@strix/gameContext";
import { useUser } from "@strix/userContext";
import List from "@mui/material/List";
import Divider from "@mui/material/Divider";
import s from "./css/navbar.module.css";
import OverviewButton from "./OverviewButton";
import NavigationSection from "./NavigationSection";
import UserMenu from "./UserMenu";
import GameSelector from "./GameSelector";
import AspectSwitcher from "./AspectSwitcher";
import { NAVBAR_ASPECTS } from "./utils/navbarConstants";

const Navbar = () => {
  const { game } = useGame();
  const { userProfile } = useUser();
  const navigate = useNavigate();
  const location = useLocation();

  // Local UI state
  const [currentOpenedPage, setOpenedPage] = useState(location.pathname);
  const [currentAspect, setCurrentAspect] = useState(NAVBAR_ASPECTS.ANALYTICS);

  // Memoized values
  const navbarDisabled = useMemo(() => !game?.gameID, [game?.gameID]);

  // Event handlers
  const handleAspectChange = useCallback((newAspect) => {
    setCurrentAspect(newAspect);
  }, []);

  // Update current page when location changes
  useEffect(() => {
    setOpenedPage(location.pathname);
  }, [location.pathname]);

  return (
    <div className={s.navbarContainer}>
      <div className={s.navbarInnerContainer}>
        <div
          style={{
            paddingLeft: 16,
            marginRight: 6,
            marginBottom: 16
          }}
        >
          <StrixLogo width={150} height={30} />
        </div>

        {/* Overview Button */}
        <div className={s.overviewButtonContainer}>
          <List
            sx={{
              width: "100%",
              maxWidth: 360,
              paddingLeft: "1rem",
              paddingRight: "1rem",
              boxSizing: "border-box",
            }}
            component="nav"
          >
            <OverviewButton
              currentOpenedPage={currentOpenedPage}
              setOpenedPage={setOpenedPage}
              navigate={navigate}
            />
          </List>
        </div>

        {/* Game Selector */}
        <div className={s.gameSelectorSection}>
          <GameSelector />
        </div>

        <Divider sx={{ mx: 2, my: 1, opacity: 0.3 }} />

        {/* Aspect Switcher */}
        <div className={s.aspectSwitcherSection}>
          <AspectSwitcher
            currentAspect={currentAspect}
            onAspectChange={handleAspectChange}
            disabled={navbarDisabled}
          />
        </div>

        {/* Navigation Categories - Always Expanded */}
        <NavigationSection
          navbarDisabled={navbarDisabled}
          currentOpenedPage={currentOpenedPage}
          setOpenedPage={setOpenedPage}
          navigate={navigate}
          currentAspect={currentAspect}
        />
      </div>

      {/* User Menu */}
      <UserMenu
        userProfile={userProfile}
        setOpenedPage={setOpenedPage}
        navigate={navigate}
      />
    </div>
  );
};
